def _tectonic_available() -> bool:
    try:
        ensure_tectonic()
        return True
    except TectonicNotAvailableError:
        return False


requires_tectonic = pytest.mark.tectonic
skip_without_tectonic = pytest.mark.skipif(
    not _tectonic_available(),
    reason="no tectonic binary on PATH/cache and none could be downloaded",
)


@requires_tectonic
@skip_without_tectonic
class TestRealCompiles:
    def test_audit_pdf_compiles_for_the_fixture(self, tmp_path):
        out = write_equation_audit(EQUATIONS_DOCX, tmp_path, compile_pdf=True)

        assert out.audit_pdf_path is not None, out.audit_md_path.read_text()
        assert out.audit_pdf_path.is_file()
        assert out.audit_pdf_path.stat().st_size > 0
        # Every real equation in this fixture compiles cleanly together, so
        # the fast combined-document path succeeds and no isolated probing
        # is needed -- compile_statuses stays empty.
        assert out.compile_statuses == ()

    def test_cli_equations_pdf_flag_compiles(self, tmp_path):
        output = tmp_path / "audit"
        cli_result = runner.invoke(
            app,
            ["equations", str(EQUATIONS_DOCX), "--output", str(output), "--pdf"],
        )

        assert cli_result.exit_code == 0, cli_result.output
        assert "compiled" in cli_result.output
        assert (output / "audit.pdf").is_file()

    def test_broken_equation_is_isolated_and_marked_failed_not_fatal(self, tmp_path):
        """A single unbalanced-brace equation must not take down the audit --
        it gets isolated by per-equation probing, marked FAILED, and shown as
        raw text so the surrounding equations still compile into audit.pdf."""
        equations = (
            EquationRecord(index=0, display=False, paragraph_snippet="ok one", latex="a + b"),
            EquationRecord(
                index=1, display=True, paragraph_snippet="broken one", latex="\\frac{a}{"
            ),
            EquationRecord(index=2, display=False, paragraph_snippet="ok two", latex="c^2"),
        )

        pdf_path, statuses = _compile_audit_pdf(equations, tmp_path)

        assert pdf_path is not None
        assert pdf_path.is_file()
        by_index = {s.index: s for s in statuses}
        assert by_index[0].ok is True
        assert by_index[1].ok is False
        assert by_index[1].message  # some diagnostic text was captured
        assert by_index[2].ok is True

    def test_probe_compile_equations_isolates_bad_equation_from_good_ones(self, tmp_path):
        equations = (
            EquationRecord(index=0, display=False, paragraph_snippet="ok", latex="x^2"),
            EquationRecord(
                index=1, display=False, paragraph_snippet="bad", latex="\\undefinedmacro{oops}"
            ),
        )
        statuses = probe_compile_equations(equations)
        by_index = {s.index: s for s in statuses}
        assert by_index[0].ok is True
        assert by_index[1].ok is False
