def test_mismatch_flag_when_pandoc_drops_an_equation(monkeypatch):
    """Simulate pandoc converting fewer equations than the raw OMML walk found."""
    import latextify.audit.equations as audit_equations

    monkeypatch.setattr(
        audit_equations,
        "_pandoc_math_nodes",
        lambda docx_path: [pf.Math("a+b", format="InlineMath")],
    )
    monkeypatch.setattr(
        audit_equations,
        "_walk_raw_equations",
        lambda document_root: [(False, "first"), (True, "second"), (False, "third")],
    )

    out = audit_equations.extract_equations(EQUATIONS_DOCX)

    assert out.raw_omml_count == 3
    assert out.converted_count == 1
    assert out.count_mismatch is True
    # The equation pandoc did convert keeps its real pairing...
    assert out.equations[0].latex == "a+b"
    # ...the ones pandoc dropped get an empty latex rather than a guess.
    assert out.equations[1].latex == ""
    assert out.equations[2].latex == ""
    # Raw-XML snippets are never lost, mismatch or not.
    assert [e.paragraph_snippet for e in out.equations] == ["first", "second", "third"]


def test_mismatch_flag_when_pandoc_invents_an_equation(monkeypatch):
    """Simulate pandoc producing MORE Math nodes than the raw OMML walk found."""
    import latextify.audit.equations as audit_equations

    monkeypatch.setattr(
        audit_equations,
        "_pandoc_math_nodes",
        lambda docx_path: [
            pf.Math("a", format="InlineMath"),
            pf.Math("b", format="DisplayMath"),
        ],
    )
    monkeypatch.setattr(
        audit_equations, "_walk_raw_equations", lambda document_root: [(False, "only one")]
    )

    out = audit_equations.extract_equations(EQUATIONS_DOCX)

    assert out.raw_omml_count == 1
    assert out.converted_count == 2
    assert out.count_mismatch is True
    assert out.equations[0].paragraph_snippet == "only one"
    assert out.equations[0].latex == "a"
    # The "invented" equation has no raw-XML counterpart -- empty snippet,
    # display flag falls back to pandoc's own Math.format classification.
    assert out.equations[1].paragraph_snippet == ""
    assert out.equations[1].latex == "b"
    assert out.equations[1].display is True


