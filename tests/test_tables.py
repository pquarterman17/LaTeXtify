@pytest.mark.parametrize("journal_name", ["revtex4-2", "ieeetran", "elsarticle", "sn-jnl"])
def test_pathological_table_emits_compilable_body_per_journal(tmp_path, journal_name):
    """``emit_project`` never touches ``longtable``/``multirow`` for a
    pathological table, in every journal: the content survives (duplicated,
    not dropped), the bold in-document note is present, and the finding
    still surfaces as a warning on the ``EmitResult``. Does not compile
    (that's the tectonic-marked tests below) -- this runs unconditionally so
    the content contract is checked even without a Tectonic binary available.
    """
    docx = _copy_fixture(tmp_path, TABLES_DOCX)
    result = emit_project(docx, journal_name, tmp_path / "output", report=False)

    body = result.body_tex_path.read_text(encoding="utf-8")
    assert "Measurement" in body
    assert "\\multirow" not in body
    assert "\\longtable" not in body
    assert "[table structure simplified -- verify against source]" in body

    assert any("vertically merged cell (vMerge)" in w.message for w in result.warnings)


@pytest.mark.tectonic
@pytest.mark.skipif(
    not _tectonic_available(),
    reason="no tectonic binary on PATH/cache and none could be downloaded",
)
@pytest.mark.parametrize("journal_name", ["revtex4-2", "elsarticle", "sn-jnl"])
def test_pathological_table_compiles_end_to_end_per_journal(tmp_path, journal_name):
    """Plan item 25's core done-when: a manuscript containing the
    pathological table (tables.docx) compiles to PDF through the real
    ``emit_project`` -> Tectonic pipeline -- including revtex4-2's two-column
    ``reprint`` mode, which pandoc's own default longtable-based table
    rendering could not have cleared either (see
    ``test_body_compiles_in_two_column_harness`` and the ingest.filters
    module docstring).

    ieeetran is deliberately excluded here and covered instead by
    ``test_ieeetran_real_preamble_compiles_pathological_table`` below: this
    fixture has zero citations, and a genuinely PRE-EXISTING, unrelated bug
    (reproduced independently with the citation-free clean.docx fixture, so
    it is not a tables/item-25 regression) makes ``emit_project``'s
    unconditional ``\\bibliography{references}`` call in ``main.tex`` fail to
    compile against IEEEtran specifically for ANY citation-free manuscript --
    ``LaTeX Error: Something's wrong--perhaps a missing \\item.`` at
    ``\\end{thebibliography}`` in the BibTeX-generated ``main.bbl`` (IEEEtran's
    own ``\\thebibliography`` redefinition does not tolerate zero
    ``\\bibitem``s, unlike revtex4-2/elsarticle/sn-jnl, which all compile fine
    here with the same empty bibliography). Out of scope for item 25 --
    see the executor report.
    """
    docx = _copy_fixture(tmp_path, TABLES_DOCX)
    result = emit_project(docx, journal_name, tmp_path / "output", report=False)

    journal = templates_loader.load(journal_name)
    vendor_dir = journal.root / "vendor" if journal.vendor else None
    compile_result = compile_document(
        result.main_tex_path, tectonic_path=ensure_tectonic(), vendor_dir=vendor_dir
    )
    assert compile_result.success, compile_result.raw_log
    assert compile_result.pdf_path is not None
    assert compile_result.pdf_path.is_file()
    assert compile_result.pdf_path.stat().st_size > 0


@pytest.mark.tectonic
@pytest.mark.skipif(
    not _tectonic_available(),
    reason="no tectonic binary on PATH/cache and none could be downloaded",
)
def test_ieeetran_real_preamble_compiles_pathological_table(tables_result, tmp_path):
    """ieeetran's real preamble (genuine two-column ``journal`` class option,
    exactly as rendered by ``templates.loader``) plus the degraded
    pathological table body -- built directly rather than through
    ``emit_project``, to isolate this from the pre-existing, unrelated
    citation-free-bibliography bug documented in
    ``test_pathological_table_compiles_end_to_end_per_journal`` above (this
    fixture has no citations, so no ``\\bibliography`` call is needed to
    prove the actual thing item 25 is responsible for: the real ieeetran
    twocolumn preamble plus our degraded table compiling together).
    """
    journal = templates_loader.load("ieeetran")
    preamble = journal.render_preamble()
    tex = f"{preamble}\n\\begin{{document}}\n{tables_result.tex}\n\\end{{document}}\n"
    tex_path = tmp_path / "main.tex"
    tex_path.write_text(tex, encoding="utf-8", newline="")

    compile_result = compile_document(tex_path, tectonic_path=ensure_tectonic())
    assert compile_result.success, compile_result.raw_log
    assert compile_result.pdf_path is not None
    assert compile_result.pdf_path.is_file()
    assert compile_result.pdf_path.stat().st_size > 0
